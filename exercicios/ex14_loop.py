#Contagem regressiva:
#Escreva um programa que solicita ao usuário um número inteiro positivo e, em seguida, 
#conta regressivamente desse número até zero, imprimindo cada número no processo.

#entrada
numero = int(input("DIgite um número: "))
contagem = 0
#processamento
while numero >= contagem:
    print(numero)
    numero -= 1

